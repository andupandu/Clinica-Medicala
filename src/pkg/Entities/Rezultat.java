package pkg.Entities;

public class Rezultat {
	String valoare;
	String interpretare;
	String data;
	String pacient;
	String analiza;
	public String getValoare() {
		return valoare;
	}
	public void setValoare(String valoare) {
		this.valoare = valoare;
	}
	public String getInterpretare() {
		return interpretare;
	}
	public void setInterpretare(String interpretare) {
		this.interpretare = interpretare;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getPacient() {
		return pacient;
	}
	public void setPacient(String pacient) {
		this.pacient = pacient;
	}
	public String getAnaliza() {
		return analiza;
	}
	public void setAnaliza(String analiza) {
		this.analiza = analiza;
	}
	
}
